<h1>Continuous Localization</h1>

<p>Historically, localization has been adding a whole new layer of technical complexity and management overhead into product life cycle. Things would get even more challenging in agile product development cycle, when there are frequent software releases, and a constant inflow of new material to translate, review and publish.</p>

<p>Continuous localization revolutionizes this process much like continuous build integration revolutionized software development process. <strong>It is a way of automatically, seamlessly gathering new source material, publishing it for translation, acquiring translations and integrating them back into the product.</strong> Ultimately, it makes manual localization management — exporting, converting files, sending them for translation, doing backward conversion, committing changes to version control — unnecessary.</p>

<p>Imagine that developers only ever need to deal with one source language, and — magically! — see all localized resources being updated in their version control system by a tireless localization robot as soon as translations arrive. This is exactly what Serge helps to achieve. Building robust multi-platform multilingual products in agile development environment suddenly becomes a manageable goal.</p>

